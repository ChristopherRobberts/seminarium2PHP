          Swedish traditional dishes, some of which are many hundreds of years old, others perhaps a century
          or less, are still a very important part of Swedish everyday meals, in spite
          of the fact that modern day Swedish cuisine adopts many international dishes.
          Internationally, the most renowned Swedish culinary tradition is the smorgasbord and,
          at Christmas, the julbord, including well known Swedish dishes such as gravlax and meatballs.
          In Sweden, traditionally, Thursday has been soup day because the maids had half
          the day off and soup was easy to prepare in advance. One of the most
          traditional Swedish soups, artsoppa is still served in many restaurants and
          households every Thursday, a tradition since the middle ages. artsoppa
          is a yellow pea soup, commonly served with pancakes as dessert. This is a
          simple meal, a very thick soup, basically consisting of boiled yellow
          peas, a little onion, salt and small pieces of pork. It is often served
          with mustard and followed by a dessert of thin pancakes (see pancake page).
          The Swedish Armed Forces also serve their conscripts pea soup and
          pancakes every Thursday.
          Potatoes are eaten year-round as the main source of carbohydrates, and are a
          staple in many traditional dishes. Not until the last 50 years have pasta or
          rice become common on the dinner table. There are several different kinds of
          potatoes: the most appreciated is the new potato, a potato which ripens in
          early summer, and is enjoyed at the traditional midsummer feast. New potatoes
          at midsummer are served with pickled herring, chives, sour cream, and the
          first strawberries of the year are traditionally served as dessert.
          The most highly regarded mushroom in Sweden is the chanterelle, which is
          considered a delicacy. The chanterelle is usually served as a side dish together
          with steaks, or fried with onions and sauce served on an open sandwich. Second
          to the chanterelle, and considered almost as delicious, is the porcini mushroom,
          or karljohansvamp, named after Charles XIV John (Karl XIV Johan) who introduced
          its use as food.
